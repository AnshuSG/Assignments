package src.Assignments.Polymorphism.MethodOverloading;

public class atb5xCourse {

    //Method overloading with different type of parameters or different number of parameters
    void oops(String Course1)
    {
        System.out.println(Course1);
    }

    void oops(String course1, String course2)
    {
        System.out.println(course1  + " and " + course2);
    }
}
